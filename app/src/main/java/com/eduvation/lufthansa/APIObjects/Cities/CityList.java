    public static String getCityName(String cityCode) {

        int iterator = 0;

        if (cityCode != null) {
            while (iterator < cities.length) {

                if (cityCode.equals(cities[iterator].getCityCode()))
                    return cities[iterator].getName();

                ++iterator;
            }
        }

        return "";
    }

