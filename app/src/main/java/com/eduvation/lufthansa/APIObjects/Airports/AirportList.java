    // return citycode according to airportcode
    public static String getAirportCityCode(String airportCode) {

        int iterStopper = 0;
        while ( iterStopper < airports.length ) {
            if(airportCode.equals(airports[iterStopper].getAirportCode()))
                return airports[iterStopper].getCityCode();

            ++iterStopper;
        }

        return null;
    }

    // return airport name according to airportCode
    public static String getAirportName(String airportCode) {
        int i = 0;
        while(i<airports.length) {
            if (airports[i].getAirportCode().equals(airportCode))
                return airports[i].getName();

            ++i;
        }

        return "";
    }
